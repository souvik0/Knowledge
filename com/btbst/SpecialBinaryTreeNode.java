package com.btbst;

public class SpecialBinaryTreeNode {

    Integer value;
    SpecialBinaryTreeNode left;
    SpecialBinaryTreeNode right;
    SpecialBinaryTreeNode next_right;

    SpecialBinaryTreeNode(Integer value) {
         this.value = value;
         this.left = null;
         this.right = null;
         this.next_right = null;
     }
}
